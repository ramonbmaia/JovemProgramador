let bebida = 2;

switch (bebida) {
    case 1:
        console.log("Água");
        break;
    case 2:
        console.log("Refrigerante");
        break;
    case 3:
        console.log("Chá");
        break;
    case 4:
        console.log("Suco");
        break;
    case 5:
        console.log("Energético");
        break;
    default:
        console.log("Item não cadastrado!");
        break;
}
